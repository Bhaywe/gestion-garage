     Témoins
     Si vous laissez un commentaire sur notre site, il vous sera proposé d’enregistrer votre nom, adresse courriel et site web dans des témoins. C’est uniquement pour vous simplifier la vie afin de ne pas avoir à saisir ces informations si vous laissez un autre commentaire plus tard. Ces témoins expirent au bout d’un an.

     Si vous vous rendez sur la page de connexion, un témoin temporaire sera créé afin de déterminer si votre navigateur accepte les témoins. Il ne contient pas de données personnelles et sera supprimé automatiquement à la fermeture de votre navigateur.

     Lorsque vous vous connecterez, nous mettrons en place un certain nombre de témoins pour enregistrer vos informations de connexion et vos préférences d’écran. La durée de vie d’un témoin de connexion est de deux jours, celle d’un témoin d’option d’écran est d’un an. Si vous cochez « Se souvenir de moi », votre témoin de connexion sera conservé pendant deux semaines. Si vous vous déconnectez de votre compte, le témoin de connexion sera effacé.

     En modifiant ou en publiant un article, un témoin supplémentaire sera enregistré dans votre navigateur. Ce témoin ne comprend aucune donnée personnelle. Il indique simplement l’identifiant de l’article que vous venez de modifier. Il expire au bout d’une journée.

     Contenu imbriqué d’autres sites web
     Les articles de ce site peuvent inclure des contenus intégrés (par exemple des vidéos, images, articles, etc). Le contenu intégré à partir d’autres sites se comporte de la même manière que si le visiteur se rendait sur cet autre site.

     Ces sites web pourraient recueillir des données sur vous, utiliser des témoins, installer des outils de suivi tierce-partie ou suivre vos interactions avec ces contenus intégrés si vous disposez d’un compte et êtes connecté leur site web.

     Statistiques et mesures d’audience
     Avec qui partageons-nous vos données
     Pendant combien de temps vos données sont-elles conservées ?
     Si vous laissez un commentaire, le commentaire et ses métadonnées sont conservés indéfiniment. Ceci permet de reconnaître et approuver automatiquement les commentaires suivants au lieu de les laisser dans la file de modération.

     Pour les utilisateurs qui s’enregistrent sur notre site (si cela est possible), nous conservons également les données personnelles indiquées dans leur profil. Tous les utilisateurs peuvent voir, modifier ou supprimer leurs informations personnelles à tout moment (à l’exception de leur nom d’utilisateur). Les gestionnaires du site peuvent aussi voir et modifier ces informations.

     Quels sont vos droits sur vos données ?
     Si vous avez un compte ou si vous avez laissé des commentaires sur ce site, vous pouvez demander à recevoir un fichier contenant toutes les données personnelles que nous possédons à votre sujet, incluant celles que vous nous avez fournies. Vous pouvez également demander la suppression des données personnelles vous concernant. Ceci ne prend pas en compte les données conservées à des fins administratives, légales ou pour des raisons de sécurité.

     Transmission de vos données personnelles
     Les commentaires des visiteurs peuvent être vérifiés à l’aide d’un service automatisé de détection des commentaires indésirables.

